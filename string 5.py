s = "hello world" 
print(s.capitalize())  # Converts the first character to uppercase
print("HELLO".casefold())  # Converts string into lower case (stronger than lower)
print("hi".center(6))  # Centers the string with spaces
print(s.count("l"))  
# Counts how many times a value appears
print(s.endswith("world"))  
# Checks if string ends with value
print(s.find("world"))  
# Finds position of value, -1 if not found
print("I am {}".format("happy"))  
# Formats values into string
